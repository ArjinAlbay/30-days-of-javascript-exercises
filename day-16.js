// 1- Change skills array to JSON using JSON.stringify()

let skills = ["HTML", "CSS", "JS", "React", "Node", "Python"];

let jsonSkills = JSON.stringify(skills);

console.log(jsonSkills); // ["HTML","CSS","JS","React","Node","Python"]

// 2- Stringify the age variable

let age = 250;

let jsonAge = JSON.stringify(age);

console.log(typeof jsonAge); // string
console.log(jsonAge); // 250

// 3- Stringify the isMarried variable

let isMarried = true;

let jsonMarried = JSON.stringify(isMarried);

console.log(typeof jsonMarried); // string
console.log(jsonMarried); // true

